DROP SCHEMA IF EXISTS iso CASCADE;

CREATE SCHEMA IF NOT EXISTS iso;

-- continents
CREATE TABLE
    IF NOT EXISTS iso.continents (
        "id" CHAR(2) NOT NULL PRIMARY KEY,
        "name" VARCHAR(20) NOT NULL
    );

-- countries
CREATE TABLE
    IF NOT EXISTS iso.countries (
        "id" CHAR(2) PRIMARY KEY,
        "continent" CHAR(2) NOT NULL,
        "name" VARCHAR(100) NOT NULL,
        "flag" CHAR(2) NOT NULL,
        "tld" CHAR(2) NOT NULL,
        "prefix" VARCHAR(100) NOT NULL,
        "is_eu" BOOLEAN NOT NULL DEFAULT FALSE,
        enabled BOOLEAN NOT NULL DEFAULT FALSE,
        CONSTRAINT "fk_continent" FOREIGN KEY ("continent") REFERENCES iso.continents ("id")
    );

--languages
CREATE TABLE
    IF NOT EXISTS iso.languages (
        "id" CHAR(3) PRIMARY KEY,
        "name" VARCHAR(100) UNIQUE,
        "enabled" BOOLEAN NOT NULL DEFAULT FALSE
    );

-- country_languages
CREATE TABLE
    IF NOT EXISTS iso.country_languages (
        "id" SERIAL PRIMARY KEY,
        "country" CHAR(2) NOT NULL,
        "language" CHAR(3) NOT NULL,
        "enabled" BOOLEAN NOT NULL DEFAULT FALSE,
        CONSTRAINT "fk_country" FOREIGN KEY ("country") REFERENCES iso.countries ("id"),
        CONSTRAINT "fk_language" FOREIGN KEY ("language") REFERENCES iso.languages ("id"),
        UNIQUE ("country", "language")
    );

-- currencies
CREATE TABLE
    IF NOT EXISTS iso.currencies (
        "id" CHAR(3) NOT NULL PRIMARY KEY,
        "name" VARCHAR(100) NOT NULL
    );

-- country_currencies
CREATE TABLE
    IF NOT EXISTS iso.country_currencies (
        "id" SERIAL PRIMARY KEY,
        "country" CHAR(2),
        "currency" CHAR(3),
        "enabled" BOOLEAN NOT NULL DEFAULT FALSE,
        CONSTRAINT "fk_country" FOREIGN KEY ("country") REFERENCES iso.countries ("id"),
        CONSTRAINT "fk_currency" FOREIGN KEY ("currency") REFERENCES iso.currencies ("id"),
        UNIQUE ("country", "currency")
    );

